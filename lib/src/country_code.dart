extension CountryCodeX on CountryCode {
  String get alpha2 => name;
}

extension CountryCodeIterableX on Iterable<CountryCode> {
  Set<String> toAlpha2Set() => {for (final c in this) c.alpha2};

  /// Map-IDs anhand des Resolvers (default inkl. Overrides).
  Set<String> toMapIds({MapIdResolver mapIdResolver = defaultMapIdResolver}) {
    final out = <String>{};
    for (final c in this) {
      final id = mapIdResolver(c.alpha2);
      if (id != null) out.add(id);
    }
    return out;
  }
}
